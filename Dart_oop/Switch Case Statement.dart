void main(){
  var n= 5;

  switch(n){
    case 1:
      print('value is 1');
      break;
    case 2:
      print('value is 2');
      break;
    case 3:
      print('value is 3');
      break;
    case 4:
      print('value is 4');
      break;
    case 5:
      print('value is 5');
      break;
    default:
      print('Out of Range');
      break;
  }

}